document.addEventListener('DOMContentLoaded', () => {
    const titleInput = document.getElementById('title');
    const slugInput = document.getElementById('slug');

    if (titleInput && slugInput) {
        titleInput.addEventListener('input', function () {
            // Only update slug if it's empty or matches the previous title-generated slug
            if (!slugInput.value || slugInput.dataset.autoGenerated === 'true') {
                slugInput.value = generateSlug(this.value); // Use global function
                slugInput.dataset.autoGenerated = 'true';
            }
        });
        
        slugInput.addEventListener('input', function() {
            // Mark as manually edited if user types in slug field
            slugInput.dataset.autoGenerated = 'false';
        });
    }    // Client-side validation removed - relying on server-side validation with flash messages
});